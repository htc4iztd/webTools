	id INTEGER,
	project_id INTEGER,
	project_name VARCHAR(100),
	key_id INTEGER,
	key VARCHAR(50),
	type_id INTEGER,
	type VARCHAR(30),
	category_id INTEGER,
	category_name VARCHAR(10),
	subject VARCHAR(1000),
	description TEXT,
	status_id INTEGER,
	status VARCHAR(10),
	close_reason_id INTEGER,
	close_reason VARCHAR(10),
	incharge_id INTEGER,
	incharge VARCHAR(10),
	register_id INTEGER,
	register VARCHAR(10),
	register_date TIMESTAMP,
	parent_task_key INTEGER,
	deadline DATE,
	changer_id INTEGER,
	changer INTEGER,
	change_date TIMESTAMP,
	attached INTEGER,
	system_under_consideration VARCHAR(30),
	system_tobe_consideration VARCHAR(30),
	quarters INTEGER,
	issues_type VARCHAR(10),
	issues_seq_no VARCHAR(10),
	require_doc_capture_necessity VARCHAR(10),
